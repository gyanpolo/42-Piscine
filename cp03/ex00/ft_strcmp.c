int	ft_strcmp(char *s1, char *s2)
{
	int	c;

	c = 0;
	while (s1[c] == s2[c] && s1[c] != '\0' && s2[c] != '\0')
	{
		c++;
	}
	if (s1[c] == s2[c])
	{
		return (0);
	}
	else
	{
		return (s1[c] - s2[c]);
	}
}
