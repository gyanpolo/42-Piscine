int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	c;

	c = 0;
	if (n == 0)
	{
		return (0);
	}
	while (s1[c] == s2[c] && s1[c] != '\0' && s2[c] != '\0' && c < (n - 1))
	{
		c++;
	}
	if (s1[c] == s2[c])
	{
		return (0);
	}
	else
	{
		return (s1[c] - s2[c]);
	}
}
