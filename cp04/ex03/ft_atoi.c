int	ft_atoi(char *str)
{
	int	c;
	int	num;
	int	sig;

	sig = 1;
	c = 0;
	num = 0;
	while ((str[c] == 32) || ((str[c] >= 9) && (str[c] <= 13)))
		c++;
	while ((str[c] == 45) || (str[c] == 43))
	{
		if (str[c] == 45)
			sig *= -1;
		c++;
	}
	while (str[c] >= 48 && str[c] <= 57)
	{
		num = num * 10 + (str[c] - 48);
		c++;
	}
	return (num * sig);
}
